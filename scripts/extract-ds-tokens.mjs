#!/usr/bin/env node
/**
 * Extract a `:root` design-token layer from the compiled KP "vessel" foundation CSS.
 *
 * The vessel foundation ships colors and type as *baked literals* — there is no
 * runtime custom-property / theming layer (`var()` usage is zero). But the
 * self-documenting utility classes are effectively a token catalog:
 *
 *   .ds-colors__<name> { background-color: #bg; color: #fg; }   → color tokens
 *   .p<N>              { font-size; line-height; letter-spacing } → type scale
 *
 * This script parses those and emits `styles/ds-tokens.css` as namespaced
 * `--ds-*` custom properties, so EDS block CSS slices (Phase 4) can reference
 * `var(--ds-color-kp-blue)` instead of re-baking hex values. The output is
 * generated — never hand-edit it; re-run `npm run tokens:ds` when the
 * foundation changes.
 *
 * Namespaced under `--ds-*` deliberately: the existing styles.css `:root`
 * palette (--color-blue-500 etc.) is a different, generic scale that current
 * blocks depend on — we add alongside it, we do not touch it.
 *
 * Usage: node scripts/extract-ds-tokens.mjs [path/to/foundation.css] [out.css]
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { resolve, relative } from 'node:path';

const SRC = resolve(process.argv[2] || '.storybook/foundation/foundation.css');
const OUT = resolve(process.argv[3] || 'styles/ds-tokens.css');

const css = readFileSync(SRC, 'utf8');

/** Normalize a hex/colour value: trim, lowercase hex, collapse whitespace. */
function cleanValue(v) {
  const t = v.trim().replace(/\s+/g, ' ');
  return /^#[0-9a-fA-F]+$/.test(t) ? t.toLowerCase() : t;
}

/** Pull a single declaration value out of a rule body. */
function decl(body, prop) {
  const m = body.match(new RegExp(`(?:^|[;{])\\s*${prop}\\s*:\\s*([^;]+)`, 'i'));
  return m ? cleanValue(m[1]) : null;
}

// ---- Colors -------------------------------------------------------------
// Match `.ds-colors__<name> { ... }` regardless of the :where(...) prefix.
const colors = new Map(); // name -> { bg, fg }
const colorRe = /\.ds-colors__([a-zA-Z0-9_-]+)\s*\{([^}]*)\}/g;
for (const [, name, body] of css.matchAll(colorRe)) {
  // First definition wins (the base rule, not a later media override).
  if (!colors.has(name)) {
    const bg = decl(body, 'background-color') || decl(body, 'background');
    const fg = decl(body, 'color');
    if (bg) colors.set(name, { bg, fg });
  }
}

// ---- Type scale ---------------------------------------------------------
// `.pN { font-size; line-height; letter-spacing }` — keep the first rule per
// step that actually carries a font-size (the base, not a -modifier rule).
const type = new Map(); // n -> { size, line, spacing }
const typeRe = /(?:^|\s)\.p([0-9]+)\s*\{([^}]*)\}/g;
for (const [, n, body] of css.matchAll(typeRe)) {
  const size = decl(body, 'font-size');
  // Keep the first rule per step that carries a font-size (the base, not a -modifier).
  if (!type.has(n) && size) {
    type.set(n, {
      size,
      line: decl(body, 'line-height'),
      spacing: decl(body, 'letter-spacing'),
    });
  }
}

// ---- Emit ---------------------------------------------------------------
const lines = [];
lines.push('/*');
lines.push(' * GENERATED by scripts/extract-ds-tokens.mjs — do not edit by hand.');
lines.push(` * Source: ${relative(process.cwd(), SRC)}`);
lines.push(' * Regenerate: npm run tokens:ds');
lines.push(' *');
lines.push(' * Namespaced --ds-* token layer derived from the vessel foundation.');
lines.push(' * Separate from the generic styles.css palette; safe to import in blocks.');
lines.push(' */');
lines.push(':root {');

lines.push('  /* Colors (--ds-color-<name> = surface, --ds-on-<name> = paired foreground) */');
for (const [name, { bg, fg }] of [...colors].sort((a, b) => a[0].localeCompare(b[0]))) {
  lines.push(`  --ds-color-${name}: ${bg};`);
  if (fg) lines.push(`  --ds-on-${name}: ${fg};`);
}

if (type.size) {
  lines.push('');
  lines.push('  /* Type scale (.pN font-size / line-height / letter-spacing) */');
  for (const [n, t] of [...type].sort((a, b) => Number(a[0]) - Number(b[0]))) {
    lines.push(`  --ds-text-p${n}-size: ${t.size};`);
    if (t.line) lines.push(`  --ds-text-p${n}-line-height: ${t.line};`);
    if (t.spacing && t.spacing !== '0') lines.push(`  --ds-text-p${n}-letter-spacing: ${t.spacing};`);
  }
}

lines.push('}');
lines.push('');

writeFileSync(OUT, lines.join('\n'));
// eslint-disable-next-line no-console -- CLI build script; report what it wrote.
console.log(`Wrote ${relative(process.cwd(), OUT)} — ${colors.size} colors, ${type.size} type steps.`);
